  CREATE OR REPLACE PROCEDURE "UMS"."UPDATECARRIERPLAN" (
            p_soldierID IN NUMBER,
            p_firstCycle IN VARCHAR2,
            p_secondCycle IN VARCHAR2,
            p_thirdCycle IN VARCHAR2,
            p_fourthCycle IN VARCHAR2
        ) AS
            l_count NUMBER;
        BEGIN
            SELECT COUNT(*) INTO l_count FROM CarrierPlan WHERE PLANID = p_soldierID;

            IF l_count > 0 THEN
                UPDATE CarrierPlan
                SET FIRSTCYCLE = p_firstCycle,
                    SECONDCYCLE = p_secondCycle,
                    THIRDCYCLE = p_thirdCycle,
                    FOURTHCYCLE = p_fourthCycle
                WHERE PLANID = p_soldierID;
            ELSE
                INSERT INTO CarrierPlan (PLANID, FIRSTCYCLE, SECONDCYCLE, THIRDCYCLE, FOURTHCYCLE)
                VALUES (p_soldierID, p_firstCycle, p_secondCycle, p_thirdCycle, p_fourthCycle);
            END IF;
        END;

